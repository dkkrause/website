        <div class="capability" id="metrics">
            <h2>metrics
                <span class="ptrdiv">
                    <img class="ptrclass" id="metricsptr" src="/images/collapse.png" alt="Exp/Coll" />
                </span>
            </h2>
        </div>
        <div class="section" id="metricsbody">
        <p>
            Most companies collect lifecycle metrics. Some companies review these metrics and reach conclusions
            as to whether the lifecycle is performing well. The real question is: why are we collecting these metrics?
        </p>
        <p>
            A good set of metrics starts with goals. What do you expect from your lifecycle? How should it perform?
            This is not the time to define the metrics, but rather to understand what is needed from the
            lifecycle. Once these goals are well understood the next step is to ask: how will we know if the lifecycle
            is meeting these goals? The resulting set of questions then drives a set of metrics that answer these
            questions.
        </p>
        <p>
            For example, suppose that a company goal is to have no more than 5% deviation from schedule
            for a release. A question for this goal might be: &quot;How far off of the straight line schedule
            is this project?&quot;. The metric used to answer this question depends on your particular lifecycle
            model. A SCRUM team might use story points done against total story points in the release to understand
            whether the team is on track. A waterfall team might use earned value to understand how far through the
            product development cycle the team has gotten against the total amount of work defined.
        </p>
        <p>
            Finally, metrics must be reviewed on an ongoing basis. This can be done in many different ways. A
            SCRUM team can look at these metrics as part of the spring planning meetings and sprint review meetings.
            A waterfall team (and even a SCRUM project) probably needs regular program reviews to go over these
            metrics.
        </p>
